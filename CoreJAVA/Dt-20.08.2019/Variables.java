public class Variables {
	public static void main(String[] args) {
		int i ; //This is the declaration part of int
		i = 10 ; //Initialization part of int
		System.out.println(i); //Utilization part of int

		byte b ;//This is the declaration part of byte
		b = 120 ; //Initialization part of byte
		System.out.println(b); //Utilization part of byte

		short s ;//This is the declaration part of short
		s = 32000;//Initialization part of short
		System.out.println(s); //Utilization part of short

		long l ;//This is the declaration part of long
		l = 8249524843l; //Initialization part of long
		System.out.println(l); //Utilization part of long

		double d ;//This is the declaration part of double
		d = 2.123456789987;//Initialization part of double
		System.out.println(d); //Utilization part of double

		float f ;//This is the declaration part of float
		f = 2.1234567f;//Initialization part of float
		System.out.println(f); //Utilization part of float

		char c ;//This is the declaration part of char
		c = 'A';//Initialization part of char
		System.out.println(c); //Utilization part of char

		boolean bo;//This is the declaration part of boolean		
		bo = true ;//Initialization part of boolean
		System.out.println(bo); //Utilization part of boolean
	}
}
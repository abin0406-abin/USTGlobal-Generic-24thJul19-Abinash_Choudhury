public class HelloJava {
	public static void main(String []args) {
		byte b = 127;
		byte by = -128;
		System.out.println(b+" <--Byte--> "+by);

		short s = -32768;
		short sh = 32767;
		System.out.println(s+" <--Short--> "+sh);

		float f = 1.1234567898745623125f;
		double d = 1.123456789987654321;
		System.out.println(f+" <--FLOAT || DOUBLE-->"+d);

		char c = 'a';
		char c1 = 'A';
		System.out.println(c+" <--Character--> "+c1);

		boolean bo = true;
		boolean bool = false;
		System.out.println(bo+" <--Boolean--> "+bool);  

		System.out.println ("Hello Java");
		System.out.println("Hello World");
	}	
}